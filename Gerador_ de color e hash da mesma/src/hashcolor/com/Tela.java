    private void bntGerarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bntGerarActionPerformed
     
        if(rbGerar.isSelected()){
        Random r = new Random();
        Color draw = new Color(r.nextInt(225),r.nextInt(225),r.nextInt(225));
        txtPrint.setBackground(draw);
        txtPrint.setText(Integer.toHexString(draw.hashCode()));            
        } else if (rbInserir.isSelected()){
            int d = Integer.parseInt(txtPrint.getText());
            Color draw2 = new Color(d);
            txtPrint.setBackground(draw2);
        }
        
    }//GEN-LAST:event_bntGerarActionPerformed

